class Offer {
  final String title;
  final String price;
  final String dates;
  final String location;
  final String photoUrl;
  final String description;
  final bool isTour;

  Offer({
    required this.title,
    required this.price,
    required this.dates,
    required this.location,
    required this.photoUrl,
    required this.description,
    required this.isTour,
  });
}